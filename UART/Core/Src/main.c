/**
  * @brief System Clock Configuration
  * @retval None
  */
void SystemClock_Config(void)
{
  RCC_OscInitTypeDef RCC_OscInitStruct = {0};
  RCC_ClkInitTypeDef RCC_ClkInitStruct = {0};

  /** Configure the main internal regulator output voltage
  */
  __HAL_RCC_PWR_CLK_ENABLE();
  __HAL_PWR_VOLTAGESCALING_CONFIG(PWR_REGULATOR_VOLTAGE_SCALE3);

  /** Initializes the RCC Oscillators according to the specified parameters
  * in the RCC_OscInitTypeDef structure.
  */
  RCC_OscInitStruct.OscillatorType = RCC_OSCILLATORTYPE_HSI;
  RCC_OscInitStruct.HSIState = RCC_HSI_ON;
  RCC_OscInitStruct.HSICalibrationValue = RCC_HSICALIBRATION_DEFAULT;
  RCC_OscInitStruct.PLL.PLLState = RCC_PLL_NONE;
  if (HAL_RCC_OscConfig(&RCC_OscInitStruct) != HAL_OK)
  {
    Error_Handler();
  }

  /** Initializes the CPU, AHB and APB buses clocks
  */
  RCC_ClkInitStruct.ClockType = RCC_CLOCKTYPE_HCLK|RCC_CLOCKTYPE_SYSCLK
                              |RCC_CLOCKTYPE_PCLK1|RCC_CLOCKTYPE_PCLK2;
  RCC_ClkInitStruct.SYSCLKSource = RCC_SYSCLKSOURCE_HSI;
  RCC_ClkInitStruct.AHBCLKDivider = RCC_SYSCLK_DIV1;
  RCC_ClkInitStruct.APB1CLKDivider = RCC_HCLK_DIV1;
  RCC_ClkInitStruct.APB2CLKDivider = RCC_HCLK_DIV1;

  if (HAL_RCC_ClockConfig(&RCC_ClkInitStruct, FLASH_LATENCY_0) != HAL_OK)
  {
    Error_Handler();
  }
}

/**
  * @brief USART3 Initialization Function
  * @param None
  * @retval None
  */
static void MX_USART3_UART_Init(void)
{
  huart3.Instance = USART3;
  huart3.Init.BaudRate = 115200;  // Set baud rate to 115200
  huart3.Init.WordLength = UART_WORDLENGTH_8B;  // 8-bit data length
  huart3.Init.StopBits = UART_STOPBITS_1;  // 1 stop bit
  huart3.Init.Parity = UART_PARITY_NONE;  // No parity
  huart3.Init.Mode = UART_MODE_TX_RX;  // Enable both transmit and receive
  huart3.Init.HwFlowCtl = UART_HWCONTROL_NONE;  // No hardware flow control
  huart3.Init.OverSampling = UART_OVERSAMPLING_16;  // 16x oversampling
  huart3.Init.OneBitSampling = UART_ONE_BIT_SAMPLE_DISABLE;  // Disable one-bit sampling
  if (HAL_UART_Init(&huart3) != HAL_OK)
  {
    Error_Handler();
  }
}

/**
  * @brief GPIO Initialization Function
  * @param None
  * @retval None
  */
static void MX_GPIO_Init(void)
{
  GPIO_InitTypeDef GPIO_InitStruct = {0};

  /* GPIO Ports Clock Enable */
  __HAL_RCC_GPIOB_CLK_ENABLE();

  /* Configure GPIO pins : PB10 USART3_TX and PB11 USART3_RX */
  GPIO_InitStruct.Pin = GPIO_PIN_10 | GPIO_PIN_11;
  GPIO_InitStruct.Mode = GPIO_MODE_AF_PP;  // Set pins to alternate function mode (push-pull)
  GPIO_InitStruct.Pull = GPIO_NOPULL;  // No pull-up, pull-down resistors
  GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_VERY_HIGH;  // Set GPIO speed
  GPIO_InitStruct.Alternate = GPIO_AF7_USART3;  // Set alternate function for USART3
  HAL_GPIO_Init(GPIOB, &GPIO_InitStruct);  // Initialize GPIO with the specified settings
}

